package data;

public class Product {
	String ProductId = null;
	String ProductName = null;
	String CategoryId = null;
	String BarCode = null;
	String Price =null;
	String Caducidad = null;
	String CapacityId= null;
	String TotalProduct = null;
	String LimitMin = null;
	
	public String getProductId() {
		return ProductId;
	}
	public void setProductId(String productId) {
		ProductId = productId;
	}
	public String getProductName() {
		return ProductName;
	}
	public void setProductName(String productName) {
		ProductName = productName;
	}
	public String getCategoryId() {
		return CategoryId;
	}
	public void setCategoryId(String categoryId) {
		CategoryId = categoryId;
	}
	public String getBarCode() {
		return BarCode;
	}
	public void setBarCode(String barCode) {
		BarCode = barCode;
	}
	public String getPrice() {
		return Price;
	}
	public void setPrice(String price) {
		Price = price;
	}
	public String getCaducidad() {
		return Caducidad;
	}
	public void setCaducidad(String caducidad) {
		Caducidad = caducidad;
	}
	public String getCapacityId() {
		return CapacityId;
	}
	public void setCapacityId(String capacityId) {
		CapacityId = capacityId;
	}
	public String getTotalProduct() {
		return TotalProduct;
	}
	public void setTotalProduct(String totalProduct) {
		TotalProduct = totalProduct;
	}
	public String getLimitMin() {
		return LimitMin;
	}
	public void setLimitMin(String limitMin) {
		LimitMin = limitMin;
	} 
	
	
}
